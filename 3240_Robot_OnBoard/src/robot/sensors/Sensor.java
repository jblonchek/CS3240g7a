package robot.sensors;
/**
 * Sensor values range from 0.0 to 1.0
 * SENSORCS,	Color sensor		, not connected
 * SENSORUS		Ultrasonic sensor	, port 1
 * SENSORSS,	Sound sensor		, port 4
 * SENSORTS,	Touch sensor		, port 3
 * SENSORLS		Light sensor		, port 2
 * 
 * @author cceckman
 */
public enum Sensor {
	SENSORCS,
	SENSORUS,
	SENSORSS,
	SENSORTS,
	SENSORLS
}
